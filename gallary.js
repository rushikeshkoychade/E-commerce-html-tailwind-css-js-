function displayImages() {
    for (let i = 1; i <= 6; i++) {
        const imageDiv = document.getElementById(`img-${i}`);
        const imgTag = imageDiv.querySelector('img');
        if (imgTag) {
            const imageArray = imagesData[`images-${i}`];
            if (imageArray && imageArray.length > 0) {
                imgTag.src = getRandomImage(imageArray);
            } else {
                console.error(`No images found for img-${i}`);
            }
        }
    }
}

function getRandomImage(images) {
    const randomIndex = Math.floor(Math.random() * images.length);
    console.log(`Random Image: ${images[randomIndex]}`); // Debugging line
    return images[randomIndex];
}


